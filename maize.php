<h1>5 major diseases of maize and their management</h1>

<p>Cotton, the commercial crop is the backbone of the textile industry as it employs 
vast majority of population directly or indirectly and earns the foreign exchange too.
The insect pests spectrum of cotton is quite complex and as many as 1326 species of 
insect pests have been listed on this crop throughout the world. However, main losses 
in cotton production are due to its susceptibility to about 162 species of insect pests.</p>

<p>Among these, the bollworms viz., American bollworm, Helicoverpa armigera, (Hubner), 
spotted bollworm, Earias vittella (Fabricius), spiny bollworm, Earias insulana (Biosdual),
 pink bollworm, Pectinophora gossypiella (Saunders), pose greater threat to cotton production.
 Besides these, a complex of sucking pests viz., green leaf hopper, Amrasca biguttula biguttula
 (Ishida), thrips, Thrips tabaci (Linnman), aphids, Aphis gossypii (Glover), whitefly, Bemisia
 tabaci (Gennadius), red cotton bug, Dysdercus koenigii (Fabricius) and dusky cotton bug, Oxycaranu
 shayalinipennis (Costa) occupy major pest status and contribute to lower yields.</p>
 
 <h5>1. Bacterial Stalk Rot:</h5>
 <p><strong>Causal organism:</strong>Erwinia carotovora, Erwinia chrysanthemi</p>
 <img src="di-images/dm1.jpg" height="300px" width="200px"/>
  <img src="di-images/dm2.jpg" height="300px" width="200px"/><br/>
  <strong>symptoms:</strong>
  <ul>
  <li>The basal internodes develop soft rot and give a water soaked appearance. A mild sweet fermenting odour accompanies such rotting.</li>
  <li>Leaves some time show signs of wilting or water loss and affected plants within a few days of infection lodge or topple down.</li>
  <li>Ears and shank may also show rot. They fail to develop further and the ears hang down simply from the plant</li>
  </ul>
  
  <strong>control measures:</strong>
  <ul>
  <li>Use of disease resistance varieties, i.e. Hybrids Ganga Safed-2, DHM 103, show significantly less disease incidence than other hybrids.</li>
  <li>Avoid waterlogging and poor drainage.</li>
  </ul>
